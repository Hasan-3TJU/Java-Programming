
package substring;

import java.util.Scanner;
public class Substring {

    public static void main(String[] args) {
      Scanner s=new Scanner(System.in);
      String s1=s.next();//Lieutenant
      
      System.out.println(s1.substring(5));//enant
      System.out.println(s1.substring(0, 7));//Lieuten
    }
    
}
